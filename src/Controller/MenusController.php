            $this->Flash->success(__('El Menú ha sido eliminado con éxito.'));
        } else {
            $this->Flash->error(__('El Menú no se pudo eliminar. Porfavor, intente de nuevo.'));
        }

        return $this->redirect(['action' => 'index']);
    }
}
